
public class Ex03 {

	public static void main(String[] args) {
		
		// 1. 논리형
		boolean a = true;
		boolean b = false;
		
		
		// 2. 문자형
		// char에는 문자 하나만 저장된다 -> ' ? '
		char c = 'a';

		// 문자열을 선언하는 방법 -> 기본데이터 자료형이 아닌 '참조 자료형' 사용!
		String d = "abc";
		
		// 3. 정수 -> int형이 대표!
		byte e1 = 45;
		short e2 = 2;
		int e3 = 45;
		long e4 = 10000;
		
		// 4. 실수 -> double형이 대표!
		float f = 3.1f;  //float 에는 f를 넣어야됨
		double g = 3.1; 
		
		System.out.println(f);
		System.out.println(g);
		
		
		

	}

}
